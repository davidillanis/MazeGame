    public static int[][] LaberintoAleatorio(){
        int [][] laberinto=new int[13][23];
        int filas=laberinto.length;
        int columnas=laberinto[0].length;
        //Parte 1, construir panal de laberinto
        for(int i=0; i<filas; i++){
            for(int j=0; j<columnas; j++){
                if(i%2==0){
                    laberinto[i][j]=1;
                }else if(j%2==0){
                    laberinto[i][j]=1;
                }
            }
        }
        //parte 2, abrir puertas
        for(int i=0; i<filas; i++){
            for(int j=0; j<columnas; j++){
                if(j%2!=0 && i%2!=0){
                    //laberinto[i][j+1]=5;
                    if((int)(Math.random()*2)==0){
                        laberinto[i][j+1]=0;
                    }else{
                        laberinto[i+1][j]=0;
                    }
                }
            }
        }
        //Mostrar(laberinto);
        return laberinto;
    }
    
